package claseen0;

public class Animales {
	public String perro;
	public String gato;
	public String tortuga;
	
	public Animales(String perrro, String gato, String tortuga) {
		this.perro = perro;
		this.gato = gato;
		this.tortuga = tortuga;
	}

	public String getPerro() {
		return perro;
	}

	public void setPerro(String perro) {
		this.perro = perro;
	}

	public String getGato() {
		return gato;
	}

	public void setGato(String gato) {
		this.gato = gato;
	}

	public String getTortuga() {
		return tortuga;
	}

	public void setTortuga(String tortuga) {
		this.tortuga = tortuga;
	}
	
}
